<script>
let photoSectionEl = document.getElementById("photo-section");
photoSectionEl.appendChild(getResizedDomImgEl("https://picsum.photos/500/300?random=11.jpg"));

function getResizedDomImgEl(url){   
    let imgEl = document.createElement("img");
    let img = new Image();
    img.onload = function() {
        const maxSize = 700;
        let ratio = Math.min(maxSize / this.width, maxSize / this.height);
        imgEl.src = url;
        imgEl.height = this.height * ratio;
        imgEl.width = this.width * ratio;
    };
    img.src = url;
    return imgEl;
}

let modal = document.getElementById("photoModal");

function openPhotoModal() {
    modal.style.display = "block";
}

function closePhotoModal() {
    modal.style.display = "none";
}

window.onclick = function(event) {
  if (event.target == modal) {
    closePhotoModal();
  }

}

function auto_grow(element) {
    const startDiff = 13;
    element.style.height = 0;
    element.style.height = (element.scrollHeight - startDiff) + "px";
}
</script>